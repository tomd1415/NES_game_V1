assert len(NES_PALETTE_RGB) == 64, "the NES system palette has exactly 64 entries"


def nes_rgb(index: int) -> tuple[int, int, int]:
    """Return the RGB triple for a NES colour index (masked to 6 bits)."""

    return NES_PALETTE_RGB[index & 0x3F]


def nes_qcolor(index: int) -> QColor:
    """Return the `QColor` for a NES colour index (masked to 6 bits)."""

    red, green, blue = nes_rgb(index)
    return QColor(red, green, blue)


def is_light(index: int) -> bool:
    """True when text drawn over this colour should be dark.

    Uses perceived luminance, so labels stay legible on the pale end of the
    palette (0x20-0x3F) without hand-tuning each swatch.
    """

    red, green, blue = nes_rgb(index)
    return (0.299 * red + 0.587 * green + 0.114 * blue) > 145
